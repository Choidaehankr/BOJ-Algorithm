#include <iostream>
using namespace std;
long long int arr[100001] = { 1, 3 };
// N=0) 0마리: 1.. 1
// N=1) 0마리: 1, 1마리: 2, 2마리: 0.. 1+2 = 3
// N=2) 0마리: 1, 1마리: 4, 2마리: 2.. 1+4+2 = 7
// N=3) 0마리: 1, 1마리: 6, 2마리: 8, 3마리: 2.. 1+6+8+2 = 17

// N=2일때.. {(N-2)+(N-1)}+(N-1) = {(1)+(3)}+(3) = 7
// N=3일때.. {(3)+(7)}+(7) = 17
// N=4일때는? 7 + 17 + 17 이겠죠?

int main() {
	int N;
	cin >> N;
	for (int i = 2; i <= N; i++) {
		arr[i] = (arr[i - 2] + (2*arr[i - 1])) % 9901;
	}
	cout << arr[N];
}   